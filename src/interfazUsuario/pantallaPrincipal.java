    private void txtFldDebugKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFldDebugKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            System.out.println("Me han presionado ou.");
            botonJugar();
        } else {
            if (key == KeyEvent.VK_1) {
                System.out.println("Me han presionado ou.");
                botonOpciones();

            } else {
                if (key == KeyEvent.VK_2) {
                    System.out.println("Me han presionado ou.");
                    botonCreditos();
                }
            }
        }
    }//GEN-LAST:event_txtFldDebugKeyPressed

